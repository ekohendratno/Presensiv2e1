package com.absensi.sekolah.models;

public class OrangTua {

    public String orangtua_id;
    public String orangtua_nama;
    public String orangtua_nik;
    public String orangtua_no_telp;
    public String orangtua_password;

    public OrangTua(
            String orangtua_id,
            String orangtua_nama,
            String orangtua_nik,
            String orangtua_no_telp,
            String orangtua_password){

        this.orangtua_id = orangtua_id;
        this.orangtua_nama = orangtua_nama;
        this.orangtua_nik = orangtua_nik;
        this.orangtua_no_telp = orangtua_no_telp;
        this.orangtua_password = orangtua_password;

    }
}
